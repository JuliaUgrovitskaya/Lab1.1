#include <iostream>
#include <cmath> // Для использования функции sqrt()

using namespace std;

int main() {
    double a, b, c; // Стороны треугольника
    double perimeter, area; // Периметр и площадь треугольника
    bool isIsosceles = false; // Переменная для проверки на равнобедренность

    // Ввод сторон треугольника
    cout << "Введите длины сторон треугольника: ";
    cin >> a >> b >> c;

    // Проверка на дурака (отрицательные или нулевые стороны)
    if (a <= 0 || b <= 0 || c <= 0) {
        cout << "Ошибка: введены некорректные значения сторон треугольника." << endl;
        return 1; // Завершение программы с ошибкой
    }

    // Проверка на существование треугольника
    if (a + b <= c || a + c <= b || b + c <= a) {
        cout << "Ошибка: треугольник с такими сторонами не существует." << endl;
        return 1; // Завершение программы с ошибкой
    }

    // Расчёт периметра
    perimeter = a + b + c;

    // Расчёт площади по формуле Герона
    double p = perimeter / 2; // Полупериметр
    area = sqrt(p * (p - a) * (p - b) * (p - c));

    // Проверка на равнобедренность
    if (a == b || a == c || b == c) {
        isIsosceles = true;
    }

    // Вывод результатов
    cout << "Периметр треугольника: " << perimeter << endl;
    cout << "Площадь треугольника: " << area << endl;
    if (isIsosceles) {
        cout << "Треугольник равнобедренный." << endl; //вывод равнобедренности
    }
    else {
        cout << "Треугольник не равнобедренный." << endl;
    }

    return 0; // Завершение программы без ошибок 
}
